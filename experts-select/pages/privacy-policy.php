<h3>This website uses cookies</h3>

<p>Cookies are information packets sent by web servers to web browsers, and stored by the web browsers.</p>
						<p>The information is then sent back to the server each time the browsers requests a page from the server. This enables web servers to identify and track web browsers.</p>
						<p>There are two main kinds of cookies: session cookies and persistent cookies. Session Cookies are deleted from your computer when you close your browser, whereas persistent cookies remain stored on your computer until deleted, or until they reach their expiry date.</p>

<h3>Cookies on our website</h3>
<p>Overall, cookies help us provide you with a better website, by enabling us to monitor which pages you find useful and which you don't. A cookie in no way gives us access to your computer or any information about you, other then the data you choose to share with us.</p>

<h3>Refusing Cookies</h3>
						<p>Most browsers allow you to refuse to accept cookies. Most web browsers automatically accept cookies, but you can usually modify your browser setting to decline cookies if you prefer.</p>
							<ul>
								<li>Internet Explorer; you can refuse all cookies by clicking "Tools", "Internet Options", "Privacy", and selecting "Block all cookies" using the slider selector.</li>
								<li>Firefox; you can adjust your cookie settings by clicking "Tools", "Options" and "Privacy"</li>
								<li>Google Chrome;  you can adjust cookie settings by clicking "Settings", "Show advanced settings" and under "Privacy" click "content settings"</li>
								<li>Opera; you can adjust cookie settings by clicking "Settings", and clicking on the "Privacy and Security" tab</li>
								<li>Safari; you can adjust cookie settings by clicking "Preferences" and clicking on the "Privacy" tab</li>
							</ul>
<p>Go to <a href="http://www.allaboutcookies.org" title="Additional information on Cookies">www.allaboutcookies.org</a> to find out more about website cookies.</p>

